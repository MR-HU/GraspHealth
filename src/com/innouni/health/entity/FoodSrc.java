public class FoodSrc {

	private Object foodId;
	private Object foodName;
	private Object foodNum;
	private Object foodCal;

	public Object getFoodId() {
		return foodId;
	}

	public void setFoodId(Object foodId) {
		this.foodId = foodId;
	}

	public Object getFoodName() {
		return foodName;
	}

	public void setFoodName(Object foodName) {
		this.foodName = foodName;
	}

	public Object getFoodNum() {
		return foodNum;
	}

	public void setFoodNum(Object foodNum) {
		this.foodNum = foodNum;
	}

	public Object getFoodCal() {
		return foodCal;
	}

	public void setFoodCal(Object foodCal) {
		this.foodCal = foodCal;
	}

}
